const LanguageContext = createContext({
  language: "en",
  setLanguage: () => {},
  t: (key, ...args) => {
    const value = dictionaries.en[key] || dictionaries.zh[key] || key;
    return typeof value === "function" ? value(...args) : value;
  },
});

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState("en");

  useEffect(() => {
    try {
      const savedLanguage = window.localStorage.getItem(languageStorageKey);
      if (savedLanguage === "zh" || savedLanguage === "en") {
        setLanguageState(savedLanguage);
      }
    } catch {
      setLanguageState("en");
    }
  }, []);

  const setLanguage = (nextLanguage) => {
    const normalized = normalizeLanguage(nextLanguage);
    setLanguageState(normalized);
    try {
      window.localStorage.setItem(languageStorageKey, normalized);
    } catch {
      // Ignore storage failures; language still changes for this session.
    }
  };

  const value = useMemo(() => {
    const dictionary = dictionaries[language] || dictionaries.zh;
    return {
      language,
      setLanguage,
      t: (key, ...args) => {
        const text = dictionary[key] ?? dictionaries.zh[key] ?? key;
        return typeof text === "function" ? text(...args) : text;
      },
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useI18n() {
  return useContext(LanguageContext);
}
